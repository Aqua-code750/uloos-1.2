use lazy_static::lazy_static;
use spin::Mutex;

pub const SCREEN_WIDTH: usize = 320;
pub const SCREEN_HEIGHT: usize = 200;

#[derive(Debug, Clone, Copy, PartialEq, Eq)]
#[repr(u8)]
pub enum Color {
    Black = 0,
    Blue = 1,
    Green = 2,
    Cyan = 3,
    Red = 4,
    Magenta = 5,
    Brown = 6,
    LightGray = 7,
    DarkGray = 8,
    LightBlue = 9,
    LightGreen = 10,
    LightCyan = 11,
    LightRed = 12,
    Pink = 13,
    Yellow = 14,
    White = 15,
    Teal = 54, // Custom color indexes mapped in standard VGA DAC
}

lazy_static! {
    pub static ref BACKBUFFER: Mutex<[u8; SCREEN_WIDTH * SCREEN_HEIGHT]> = Mutex::new([0; SCREEN_WIDTH * SCREEN_HEIGHT]);
}

pub struct VgaWriter {
    buffer: *mut u8,
}

unsafe impl Send for VgaWriter {}
unsafe impl Sync for VgaWriter {}

impl VgaWriter {
    pub const fn new() -> Self {
        VgaWriter {
            buffer: 0xa0000 as *mut u8,
        }
    }

    pub fn set_pixel(&self, x: usize, y: usize, color: u8) {
        if x >= SCREEN_WIDTH || y >= SCREEN_HEIGHT {
            return;
        }
        let mut backbuffer = BACKBUFFER.lock();
        let offset = y * SCREEN_WIDTH + x;
        backbuffer[offset] = color;
    }

    pub fn clear(&self, color: u8) {
        let mut backbuffer = BACKBUFFER.lock();
        unsafe {
            core::ptr::write_bytes(backbuffer.as_mut_ptr(), color, SCREEN_WIDTH * SCREEN_HEIGHT);
        }
    }

    pub fn draw_rect(&self, x: usize, y: usize, w: usize, h: usize, color: u8) {
        let mut backbuffer = BACKBUFFER.lock();
        for curr_y in y..(y + h) {
            if curr_y >= SCREEN_HEIGHT { continue; }
            let start_x = if x < SCREEN_WIDTH { x } else { continue; };
            let draw_w = if start_x + w <= SCREEN_WIDTH { w } else { SCREEN_WIDTH - start_x };
            if draw_w > 0 {
                let offset = curr_y * SCREEN_WIDTH + start_x;
                unsafe {
                    core::ptr::write_bytes(backbuffer.as_mut_ptr().add(offset), color, draw_w);
                }
            }
        }
    }

    pub fn draw_char(&self, x: usize, y: usize, c: char, color: u8) {
        let font_char = get_font_char(c);
        let mut backbuffer = BACKBUFFER.lock();
        for row in 0..8 {
            let curr_y = y + row;
            if curr_y >= SCREEN_HEIGHT { continue; }
            let bits = font_char[row];
            for col in 0..8 {
                let curr_x = x + col;
                if curr_x >= SCREEN_WIDTH { continue; }
                if (bits & (0x80 >> col)) != 0 {
                    let offset = curr_y * SCREEN_WIDTH + curr_x;
                    backbuffer[offset] = color;
                }
            }
        }
    }

    pub fn draw_string(&self, x: usize, y: usize, s: &str, color: u8) {
        let mut backbuffer = BACKBUFFER.lock();
        let mut curr_x = x;
        for c in s.chars() {
            let font_char = get_font_char(c);
            for row in 0..8 {
                let curr_y = y + row;
                if curr_y >= SCREEN_HEIGHT { continue; }
                let bits = font_char[row];
                for col in 0..8 {
                    let curr_x_char = curr_x + col;
                    if curr_x_char >= SCREEN_WIDTH { continue; }
                    if (bits & (0x80 >> col)) != 0 {
                        let offset = curr_y * SCREEN_WIDTH + curr_x_char;
                        backbuffer[offset] = color;
                    }
                }
            }
            curr_x += 8;
        }
    }

    pub fn draw_cursor(&self, x: usize, y: usize) {
        let cursor_matrix: [[u8; 6]; 8] = [
            [2, 0, 0, 0, 0, 0],
            [2, 2, 0, 0, 0, 0],
            [2, 1, 2, 0, 0, 0],
            [2, 1, 1, 2, 0, 0],
            [2, 1, 1, 1, 2, 0],
            [2, 1, 2, 2, 0, 0],
            [2, 2, 0, 0, 0, 0],
            [2, 0, 0, 0, 0, 0],
        ];
        let mut backbuffer = BACKBUFFER.lock();
        for row in 0..8 {
            let py = y + row;
            if py >= SCREEN_HEIGHT { continue; }
            for col in 0..6 {
                let px = x + col;
                if px >= SCREEN_WIDTH { continue; }
                let color_val = cursor_matrix[row][col];
                if color_val != 0 {
                    let offset = py * SCREEN_WIDTH + px;
                    backbuffer[offset] = if color_val == 1 { 15 } else { 0 };
                }
            }
        }
    }

    pub fn swap_buffers(&self) {
        let backbuffer = BACKBUFFER.lock();
        unsafe {
            core::ptr::copy_nonoverlapping(backbuffer.as_ptr(), self.buffer, SCREEN_WIDTH * SCREEN_HEIGHT);
        }
    }
}

pub static VGA: VgaWriter = VgaWriter::new();

